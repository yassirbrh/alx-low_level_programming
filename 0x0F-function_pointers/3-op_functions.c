#include <stdio.h>
#include "3-calc.h"

/**
 * op_add - Function
 *
 * Description: Returns the sum of a and b.
 *
 * @a: First value.
 * @b: Second value.
 *
 * Return: The sum of a and b.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Function
 *
 * Description: Returns the difference of a and b.
 *
 * @a: First value.
 * @b: Second value.
 *
 * Return: The difference of a and b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Function
 *
 * Description: Returns the product of a and b.
 *
 * @a: First value.
 * @b: Second value.
 *
 * Return: The product of a and b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Function
 *
 * Description: Returns the result of the division of a by b.
 *
 * @a: First value.
 * @b: Second value.
 *
 * Return: The result of the division of a by b.
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Function
 *
 * Description: Returns the remainder of the division of a by b.
 *
 * @a: First value.
 * @b: Second value.
 *
 * Return: The remainder of the division of a by b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
